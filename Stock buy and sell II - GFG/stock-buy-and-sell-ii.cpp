class Array
{
public:
    template <class T>
    static void input(vector<T> &A,int n)
    {
        for (int i = 0; i < n; i++)
        {
            scanf("%d ",&A[i]);
        }
    }

    template <class T>
    static void print(vector<T> &A)
    {
        for (int i = 0; i < A.size(); i++)
        {
            cout << A[i] << " ";
        }
        cout << endl;
    }
};


// } Driver Code Ends

class Solution {
  public:
    int stockBuyAndSell(int n, vector<int> &arr) {
       int i = 1 ;
       int profit =0;
       while(i<n){
           if(arr[i]-arr[i-1]>0){
               profit+=arr[i]-arr[i-1];
               
           }
           i++;
       }
       return profit;
    }
};


